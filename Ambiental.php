                    <h1>Campo Laboral</h1>
                    <p><strong>Gestión de Residuos:</strong> Los ingenieros ambientales pueden trabajar en la gestión de residuos sólidos, líquidos y peligrosos, diseñando sistemas de recolección, tratamiento y disposición final que minimicen el impacto ambiental y cumplan con las regulaciones.</p>

                    <p><strong>Calidad del Agua:</strong> Participan en la evaluación y el monitoreo de la calidad del agua en ríos, lagos y acuíferos, así como en el diseño y la implementación de sistemas de tratamiento de aguas residuales para eliminar contaminantes y proteger la salud pública y el medio ambiente.</p>

                    <p><strong>Calidad del Aire:</strong> Trabajan en la evaluación y el control de la contaminación atmosférica, desarrollando estrategias para reducir las emisiones de gases y partículas contaminantes provenientes de fuentes industriales, vehiculares y domésticas.</p>

                    <p><strong>Remediación de Suelos Contaminados:</strong> Se especializan en la evaluación y la limpieza de sitios contaminados por sustancias químicas peligrosas, utilizando técnicas de remediación como la extracción de suelos, la biodegradación y la inmovilización de contaminantes.</p>

                    <p><strong>Gestión Ambiental Empresarial:</strong> Trabajan en empresas e industrias para implementar prácticas y políticas de gestión ambiental que reduzcan el impacto ambiental de las operaciones y promuevan la sostenibilidad.</p>

                    <p><strong>Evaluación de Impacto Ambiental:</strong> Participan en la evaluación de proyectos y actividades humanas para identificar y prevenir posibles impactos negativos en el medio ambiente, así como en la elaboración de planes de mitigación y compensación.</p>

                    <p><strong>Consultoría Ambiental:</strong> Brindan asesoramiento a empresas, gobiernos y organizaciones en asuntos relacionados con el cumplimiento de regulaciones ambientales, la implementación de proyectos sostenibles y la gestión de riesgos ambientales.</p>

                    <p><strong>Investigación y Desarrollo:</strong> Trabajan en la investigación y el desarrollo de nuevas tecnologías, metodologías y políticas para abordar los desafíos ambientales emergentes y promover la conservación y el uso sostenible de los recursos naturales.</p>
